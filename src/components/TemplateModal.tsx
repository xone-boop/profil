'use client';

import { useEffect, useRef } from 'react';
import { PixelButton } from './PixelButton';

interface Template {
    id: string;
    slug: string;
    title: string;
    price: number;
    category: string;
    stack: string[];
    demo_url: string;
    image_url: string;
}

interface TemplateModalProps {
    template: Template | null;
    isOpen: boolean;
    onClose: () => void;
}

export function TemplateModal({ template, isOpen, onClose }: TemplateModalProps) {
    const modalRef = useRef<HTMLDivElement>(null);
    const previousActiveElement = useRef<HTMLElement | null>(null);

    useEffect(() => {
        if (!isOpen) return;

        // Store previously focused element
        previousActiveElement.current = document.activeElement as HTMLElement;

        // Focus the modal
        setTimeout(() => {
            const firstFocusable = modalRef.current?.querySelector<HTMLElement>(
                'button, a[href], [tabindex="0"]'
            );
            firstFocusable?.focus();
        }, 100);

        // Prevent body scroll
        document.body.style.overflow = 'hidden';

        const handleKeyDown = (e: KeyboardEvent) => {
            if (e.key === 'Escape') {
                onClose();
                return;
            }

            // Focus trap
            if (e.key === 'Tab') {
                const focusables = modalRef.current?.querySelectorAll<HTMLElement>(
                    'a[href], button:not([disabled]), textarea, input, select, [tabindex]:not([tabindex="-1"])'
                );
                if (!focusables?.length) return;

                const first = focusables[0];
                const last = focusables[focusables.length - 1];

                if (e.shiftKey && document.activeElement === first) {
                    e.preventDefault();
                    last.focus();
                } else if (!e.shiftKey && document.activeElement === last) {
                    e.preventDefault();
                    first.focus();
                }
            }
        };

        document.addEventListener('keydown', handleKeyDown);

        return () => {
            document.removeEventListener('keydown', handleKeyDown);
            document.body.style.overflow = 'unset';
            // Return focus to trigger
            previousActiveElement.current?.focus();
        };
    }, [isOpen, onClose]);

    if (!isOpen || !template) return null;

    const formatPrice = (price: number) => {
        return new Intl.NumberFormat('id-ID', {
            style: 'currency',
            currency: 'IDR',
            minimumFractionDigits: 0,
        }).format(price);
    };

    const handleWhatsAppOrder = () => {
        const whatsappNumber = '6283831860645';
        const message = encodeURIComponent(
            `Halo! Saya tertarik dengan template "${template.title}" seharga ${formatPrice(template.price)}. Bisa info lebih lanjut?`
        );
        window.open(`https://wa.me/${whatsappNumber}?text=${message}`, '_blank');
    };

    return (
        <div
            className="fixed inset-0 z-50 flex items-center justify-center p-4 bg-foreground/70 backdrop-blur-sm"
            onClick={onClose}
            role="dialog"
            aria-modal="true"
            aria-labelledby="modal-title"
        >
            <div
                ref={modalRef}
                className="pixel-card bg-background max-w-2xl w-full max-h-[90vh] overflow-y-auto"
                onClick={(e) => e.stopPropagation()}
            >
                {/* Header */}
                <div className="flex items-center justify-between p-4 border-b-2 border-foreground">
                    <h2 id="modal-title" className="font-pixel text-sm md:text-base text-primary">
                        {template.title}
                    </h2>
                    <button
                        onClick={onClose}
                        className="font-pixel text-lg hover:text-primary transition-colors p-2 focus-ring min-w-[44px] min-h-[44px] flex items-center justify-center"
                        aria-label="Close modal"
                    >
                        âœ•
                    </button>
                </div>

                {/* Content */}
                <div className="p-6">
                    {/* Image */}
                    <div className="aspect-video bg-surface border-2 border-foreground mb-6 overflow-hidden">
                        <img
                            src={template.image_url}
                            alt={template.title}
                            className="w-full h-full object-cover"
                        />
                    </div>

                    {/* Category & Price */}
                    <div className="flex items-center justify-between mb-4">
                        <span className="font-retro text-lg px-3 py-1 bg-surface-2 text-foreground border border-foreground">
                            {template.category}
                        </span>
                        <span className="font-pixel text-base text-primary">
                            {formatPrice(template.price)}
                        </span>
                    </div>

                    {/* Tech Stack */}
                    <div className="mb-6">
                        <h3 className="font-pixel text-xs mb-3 text-foreground">Tech Stack:</h3>
                        <div className="flex flex-wrap gap-2">
                            {template.stack.map((tech) => (
                                <span
                                    key={tech}
                                    className="font-retro text-base px-3 py-1 bg-foreground text-background"
                                >
                                    {tech}
                                </span>
                            ))}
                        </div>
                    </div>

                    {/* Actions */}
                    <div className="flex flex-col sm:flex-row gap-4">
                        <a
                            href={template.demo_url}
                            target="_blank"
                            rel="noopener noreferrer"
                            className="flex-1"
                        >
                            <PixelButton variant="secondary" className="w-full">
                                Live Demo
                            </PixelButton>
                        </a>
                        <PixelButton
                            className="flex-1"
                            onClick={handleWhatsAppOrder}
                        >
                            <svg className="w-5 h-5" viewBox="0 0 24 24" fill="currentColor">
                                <path d="M17.472 14.382c-.297-.149-1.758-.867-2.03-.967-.273-.099-.471-.148-.67.15-.197.297-.767.966-.94 1.164-.173.199-.347.223-.644.075-.297-.15-1.255-.463-2.39-1.475-.883-.788-1.48-1.761-1.653-2.059-.173-.297-.018-.458.13-.606.134-.133.298-.347.446-.52.149-.174.198-.298.298-.497.099-.198.05-.371-.025-.52-.075-.149-.669-1.612-.916-2.207-.242-.579-.487-.5-.669-.51-.173-.008-.371-.01-.57-.01-.198 0-.52.074-.792.372-.272.297-1.04 1.016-1.04 2.479 0 1.462 1.065 2.875 1.213 3.074.149.198 2.096 3.2 5.077 4.487.709.306 1.262.489 1.694.625.712.227 1.36.195 1.871.118.571-.085 1.758-.719 2.006-1.413.248-.694.248-1.289.173-1.413-.074-.124-.272-.198-.57-.347m-5.421 7.403h-.004a9.87 9.87 0 01-5.031-1.378l-.361-.214-3.741.982.998-3.648-.235-.374a9.86 9.86 0 01-1.51-5.26c.001-5.45 4.436-9.884 9.888-9.884 2.64 0 5.122 1.03 6.988 2.898a9.825 9.825 0 012.893 6.994c-.003 5.45-4.437 9.884-9.885 9.884m8.413-18.297A11.815 11.815 0 0012.05 0C5.495 0 .16 5.335.157 11.892c0 2.096.547 4.142 1.588 5.945L.057 24l6.305-1.654a11.882 11.882 0 005.683 1.448h.005c6.554 0 11.89-5.335 11.893-11.893a11.821 11.821 0 00-3.48-8.413z" />
                            </svg>
                            Order via WhatsApp
                        </PixelButton>
                    </div>
                </div>
            </div>
        </div>
    );
}
